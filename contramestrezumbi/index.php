<h6>
<span>
<p>Zumbi (Aristides Valeriano) was born in Sete Lagoas in Minas Gerais, Brazil.</p>
<p> At the age of 13, Zumbi started to practice Capoeira with Mestre Esquilo. When Esquilo joined the group Cordão de Ouro, Zumbi and other students followed. Zumbi has completely dedicated his life to Capoeira and is Contra Mestre today. </p>
<p>With his unique way of teaching Capoeira, Zumbi creates a fun and creative class each time. He combines martial arts, expressive movements and gymnastics into an exciting and challenging practice. After teaching workshops and performing throughout Europe and the Middle East, including Latvia, Israel, Austria, Sweden and Germany, Zumbi has made the US his home, where he hopes to show others the beauty and diversity of Brazilian culture. He currently gives classes in Charlotte NC.</p>
