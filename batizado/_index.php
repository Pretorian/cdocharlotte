          <h6><span>
<p>
          The International Capoeira School is hosting its 3rd annual international event!
This campaign will help bring people together, from various backgrounds and places around the world, to celebrate the hard work and dedication of the students of the International Capoeira School.
           </p><p> 
          The event, also known as a batizado and troco de cordão ceremony, is an event where those who are new to capoeira receive their first cord (belt) and those who already have a cord (belt) can be promoted. This annual event unites capoeiristas (capoeira practitioners) for a celebration, not only for their new belts, but also to celebrate their new and old friendships.</p><p> During this event there will be performances highlighting the educational and historical aspects of capoeira and workshops that will help capoeiristas expand their knowledge of capoeira.</span></h6>

