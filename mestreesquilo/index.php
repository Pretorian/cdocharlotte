                <h1>
                  Mestre Esquilo
                </h1>
                <h6><span>
 Mestre Esquilo, Bruno Antonio  de Araujo Melo, started training capoeira at the age of 10. He has been adamantly dedicated to the art of Capoeira for 23 years.  Mestre Esquilo travels the world extensively sharing his knowledge and passion for Capoeira and related Brazilian Arts.  He has been to Israel, Germany, England, France, Spain, Egypt, Russia, Turkey, Argentina and Colombia amongst other countries.               
                </span></h6>
            </div>
                <h6><span><p>
Mestre Esquilo has been a professor of Capoeira for 17 years and a Mestre for four years.  He was awarded the rank of Mestre in 2010 by his master, Mestre Suassuna.  Today Mestre Esquilo lives in Charlotte, North Carolina and is the only capoeirista holding this rank in North Carolina. Bruno has also served the community of Charlotte through exhibitions at art galleries, workshops with universities, and service projects with afterschool programs for under resourced public schools.  </p><p>
His school, The Brazilian Arts Center (BAC), opened its doors in 2012. </p><p>Through the BAC, Mestre Esquilo is playing a leading role in sharing Brazilian culture throughout the region.  The BAC provides daily training opportunities, workshops featuring Capoeira guests, and is host to international festivals featuring some of the most admired Capoeira leaders in the world today. 
Mestre Esquilo also enhances Capoeira through his original contributions to the music of the art form. </p><p>He has released three CD’s, the most recent Toque O Tambor was released in the Spring of 2013.</p><p>  
When Mestre Esquilo shares his passion for Capoeira, he does so with a sense of pride and contagious enthusiasm.</p>
                </span></h6>

